
package Mypack1;


public class MySession {
    public int id=0;
    public  long regno;
    public String name ;
    public float cgpa;

    public int getId() {
        return id;
    }

    public  void setId(int id) {
        this.id = id;
    }

    public  long getRegno() {
        return regno;
    }

    public  void setRegno(long regno) {
        this.regno = regno;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getCgpa() {
        return cgpa;
    }

    public void setCgpa(float cgpa) {
        this.cgpa = cgpa;
    }
}
